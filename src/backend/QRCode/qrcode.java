    private void initWebcam() {
        Dimension size = WebcamResolution.QVGA.getSize();
        List<Webcam> webcams = Webcam.getWebcams();

        if (webcams.isEmpty()) {
            // Xử lý trường hợp không tìm thấy webcam
            System.err.println("Không tìm thấy webcam nào.");
            return;
        }

        webcam = webcams.get(0); // Lấy webcam đầu tiên trong danh sách
        webcam.setViewSize(size);

        panel = new WebcamPanel(webcam);
        panel.setPreferredSize(size);
        panel.setFPSDisplayed(true);

        jPanel2.add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 470, 300));

        executor.execute(this);
    }

    @Override
    public void run() {
        do {
            try {
                Thread.sleep(100);
            } catch (InterruptedException e) {
                e.printStackTrace();
            }

            Result result = null;
            BufferedImage image = null;

            if (webcam.isOpen()) {
                if ((image = webcam.getImage()) == null) {
                    continue;
                }
            } else {
                break;
            }

            LuminanceSource source = new BufferedImageLuminanceSource(image);
            BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

            try {
                result = new MultiFormatReader().decode(bitmap);
            } catch (NotFoundException e) {
                //No result...
            }

            if (result != null) {
                String qrCodeData = result.getText();
                System.out.println("QR Code Data: " + qrCodeData);

                // Hiển thị dữ liệu trích xuất lên cửa sổ mới
                showExtractedData(qrCodeData);

                closeWindowAndWebcam();
            }

        } while (true);
    }

    private void showExtractedData(String data) {
        // Tạo cửa sổ mới và hiển thị dữ liệu trích xuất
        QRCodeResultWindow resultWindow = new QRCodeResultWindow(data);
        resultWindow.setVisible(true);
    }

    public void closeWindowAndWebcam() {
        webcam.close();
        setVisible(false);
        dispose();
    }

    public void onWindowClosing() {
        if (webcam != null && webcam.isOpen()) {
            webcam.close();
        }
    }

